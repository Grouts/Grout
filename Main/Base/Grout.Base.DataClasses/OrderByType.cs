namespace Grout.Base.DataClasses
{
    /// <summary>
    ///     SQL Order By Types
    /// </summary>
    public enum OrderByType
    {
        /// <summary>
        ///     Default order will be used
        /// </summary>
        None,

        /// <summary>
        ///     Will be ordered in ascending order
        /// </summary>
        Asc,

        /// <summary>
        ///     Will be ordered in descending order
        /// </summary>
        Desc
    }
}
